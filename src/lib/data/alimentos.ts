// Agrupa alimentos por categoria para fácil acesso
export function getAlimentosPorCategoria(categoria: CategoriaAlimento): Alimento[] {
  return ALIMENTOS.filter((a) => a.categoria === categoria)
}

// Agrupa alimentos por refeição
export function getAlimentosPorRefeicao(refeicao: TipoRefeicao): Alimento[] {
  return ALIMENTOS.filter((a) => a.refeicoes.includes(refeicao))
}

// Filtra por restrições alimentares
export function getAlimentosCompativeis(restricoes: RestricaoAlimentar[]): Alimento[] {
  if (restricoes.length === 0) return ALIMENTOS

  return ALIMENTOS.filter((alimento) => {
    return restricoes.every((restricao) => alimento.restricoes?.includes(restricao))
  })
}

// Busca alimento por ID
export function getAlimentoPorId(id: string): Alimento | undefined {
  return ALIMENTOS.find((a) => a.id === id)
}
