// Fun√ß√µes auxiliares
export function getReceitasPorTipo(tipo: TipoRefeicao): Receita[] {
  return RECEITAS.filter((r) => r.tipoRefeicao.includes(tipo))
}

export function getReceitasPorTag(tag: TagReceita): Receita[] {
  return RECEITAS.filter((r) => r.tags.includes(tag))
}

export function getReceitasPorCalorias(min: number, max: number): Receita[] {
  return RECEITAS.filter((r) => r.nutrientes.calorias >= min && r.nutrientes.calorias <= max)
}

export function getReceitaPorId(id: string): Receita | undefined {
  return RECEITAS.find((r) => r.id === id)
}

export function filtrarReceitas(filtros: {
  tipo?: TipoRefeicao
  tags?: TagReceita[]
  caloriasMax?: number
  tempoMax?: number
  dificuldade?: Dificuldade
}): Receita[] {
  return RECEITAS.filter((r) => {
    if (filtros.tipo && !r.tipoRefeicao.includes(filtros.tipo)) return false
    if (filtros.tags && !filtros.tags.some((tag) => r.tags.includes(tag))) return false
    if (filtros.caloriasMax && r.nutrientes.calorias > filtros.caloriasMax) return false
    if (filtros.tempoMax && r.tempoPreparo + r.tempoCozimento > filtros.tempoMax) return false
    if (filtros.dificuldade && r.dificuldade !== filtros.dificuldade) return false
    return true
  })
}
